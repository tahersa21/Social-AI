// عبارات تدل على نية واضحة — إذا احتوت الرسالة عليها، فليست تحية نقية
const INTENT_PATTERNS = [
  /\b(أريد|اريد|عايز|بغيت|نبغي|نبغى|بدي|بدّي|حاب|حابب)\b/i,
  /\b(سعر|price|prix|تكلفة|كم يكلف|بكم|بقداش|قداش|بشحال)\b/i,
  /\b(طلب|اطلب|commande|order)\b/i,
  /\b(منتج|product|produit|سلعة)\b/i,
  /\b(معلومات|معلومة|information|info)\b/i,
  /\b(مساعدة|ساعدني|help|aide)\b/i,
  /\b(كيف|كيفاش|comment|how)\b/i,
  /\b(متى|وقت|timing|quand)\b/i,
  /\b(أين|فين|وين|where|où)\b/i,
  /\b(توصيل|delivery|livraison|شحن)\b/i,
  /\b(حجز|موعد|reservation|rendez-vous)\b/i,
];

/**
 * يُحدِّد إذا كانت الرسالة تحية نقية بدون نية أخرى
 *
 * المنطق:
 *   1. تنظيف النص (حذف علامات الترقيم والإيموجي المكرر)
 *   2. البحث في قائمة التحيات الثابتة
 *   3. التحقق أن الرسالة لا تحتوي على نية واضحة (سعر، طلب، مساعدة...)
 *   4. الرسائل القصيرة جداً (≤ 3 كلمات) تُفحَص بمرونة أكبر
 */
export function isGreeting(text: string): boolean {
  const cleaned = text
    .trim()
    .replace(/[!؟?.,،؛:""''()[\]{}\-_]/g, "")
    .replace(/\s+/g, " ")
    .trim()
    .toLowerCase();

  if (!cleaned || cleaned.length > 60) return false;

  // فحص مطابقة كاملة
  if (GREETING_WORDS.has(cleaned)) return true;

  // فحص احتواء — للعبارات المركبة مثل "سلام كيف حالك"
  const wordCount = cleaned.split(" ").length;
  if (wordCount <= 4) {
    // الرسائل القصيرة: كل كلمة يجب أن تكون تحية أو كلمة تكميلية
    const hasGreetingWord = [...GREETING_WORDS].some((g) => cleaned.includes(g));
    if (hasGreetingWord) {
      // تأكيد أنه لا توجد نية واضحة
      const hasIntent = INTENT_PATTERNS.some((p) => p.test(text));
      return !hasIntent;
    }
  }

  return false;
}
